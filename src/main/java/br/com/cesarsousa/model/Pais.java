package br.com.cesarsousa.model;

public class Pais {
	
	private String sigla;
	private String nome;
	
	public Pais() {
		super();
	}
		
	public String getSigla() {
		return sigla;
	}
	
	public void setSigla(String sigla) {
		this.sigla = sigla;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}

}
